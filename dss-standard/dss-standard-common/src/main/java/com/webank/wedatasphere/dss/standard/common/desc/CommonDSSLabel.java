package com.webank.wedatasphere.dss.standard.common.desc;


import com.webank.wedatasphere.dss.common.entity.DSSLabel;

/**
 * @author: jinyangrao on 2020/09/29
 */
public class CommonDSSLabel extends DSSLabel {



    public CommonDSSLabel(){

    }

    public CommonDSSLabel(String label){
        this.label = label;
    }


    @Override
    public String getLabel() {
        return label;
    }

    @Override
    public void setLabel(String label) {
        this.label = label;
    }

    @Override
    public String toString() {
        return label;
    }
}