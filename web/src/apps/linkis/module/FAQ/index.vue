<template>
  <div>
    <iframe
      id="iframeName"
      :src="visualSrc"
      frameborder="0"
      width="100%"
      :height="height"
      v-if="isSkip"/>
    <div
      style="display: flex; justify-content: center; align-items: center;"
      :style="{'height': height + 'px'}"
      v-else>
      <span class="info">{{ info }}</span>
    </div>
  </div>
</template>
<script>
import module from './index';
import mixin from '@/common/service/mixin';
import util from '@/common/util';
export default {
  mixins: [mixin],
  data() {
    return {
      height: 0,
      visualSrc: null,
      isSkip: false,
      info: '请在跳转页面查看……',
    };
  },
  mounted() {
    this.height = this.$route.query.height;
    this.init();
  },
  methods: {
    init() {
      if (this.$route.query.isSkip) {
        const errCode = this.$route.query.errCode;
        const addr = module.data.ENVIR === 'dev' ? 'test.com' : window.location.host;
        this.visualSrc = `http://${addr}/dss/help/errorcode/${errCode}.html`;
        this.isSkip = true;
      } else {
        this.isSkip = false;
        this.info = '请在跳转页面查看……';
        this.linkTo();
      }
      if (!this.height) {
        this.height = window.innerHeight - 230;
      }
    },
    linkTo() {
      util.windowOpen(this.getFAQUrl())
    },
  },
};
</script>
<style lang="scss" scoped>
@import '@/common/style/variables.scss';
.info {
  @include font-color($light-text-color, $dark-text-color);
}
</style>